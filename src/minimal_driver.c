#include "minimal_driver.h"

/* ============================================================================================== */

#include <stdio.h>

/* ============================================================================================== */

static uart_rx_isr_callback_t uart_rx_callback = NULL;

/* ============================================================================================== */

void uart_driver_set_rx_callback(uart_rx_isr_callback_t ob)
{
    uart_rx_callback = ob;
}

/* ============================================================================================== */

void uart_isr(void)
{
    uint8_t received_byte_mock = 0xFF;
    if (uart_rx_callback)
    {
        uart_rx_callback(received_byte_mock);
    }
}

